# GitHub Push Script
# Run these commands one by one in your terminal

# Step 1: Create the repository on GitHub first!
# Go to: https://github.com/new
# Repository name: weather
# Description: A premium weather dashboard application built with vanilla JavaScript
# Public repository
# DO NOT initialize with README
# Click "Create repository"

# Step 2: After creating the repository, replace YOUR_USERNAME below with your GitHub username
# Then run these commands:

# Add remote repository (REPLACE YOUR_USERNAME!)
git remote add origin https://github.com/YOUR_USERNAME/weather.git

# Rename branch to main
git branch -M main

# Push to GitHub
git push -u origin main

# Done! Your repository is now on GitHub!
# Visit: https://github.com/YOUR_USERNAME/weather
